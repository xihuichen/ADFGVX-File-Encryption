package ie.atu.sw;





public class DirectorySpecification {
	//This class stores the input/output directory specified by user. No default directory was specified.
	//Future enhancement: include a main method to avail Unit Test for each function.
	private String inputdir=null;
	private String outputdir=null;
	public String getInputdir() {
		return inputdir;
	}
	public void setInputdir(String inputdir) {
		this.inputdir = inputdir;
	}
	public String getOutputdir() {
		return outputdir;
	}
	public void setOutputdir(String outputdir) {
		this.outputdir = outputdir;
	}}
	
	

